class TraceFileReader:
    """Reads and parses spectrum trace (.tr) files"""
    
    def __init__(self, trace_dir: str):
        self.trace_dir = trace_dir
        self.last_read_position = {}  # Track read position per file
        
    def find_latest_trace(self, pattern: str) -> str:
        """Find the most recent trace file matching pattern"""
        if not os.path.exists(self.trace_dir):
            return None
            
        files = []
        for f in os.listdir(self.trace_dir):
            if pattern in f and f.endswith('.tr'):
                files.append(os.path.join(self.trace_dir, f))
        
        if not files:
            return None
            
        # Return most recently modified
        return max(files, key=os.path.getmtime)
    
    def read_new_data(self, filepath: str, max_lines: int = 100000) -> list:
        """Read new data from trace file since last read"""
        if not filepath or not os.path.exists(filepath):
            return []
        
        # Get last read position
        last_pos = self.last_read_position.get(filepath, 0)
        
        data = []
        try:
            with open(filepath, 'r') as f:
                f.seek(last_pos)
                lines_read = 0
                
                for line in f:
                    lines_read += 1
                    if lines_read > max_lines:
                        break
                        
                    line = line.strip()
                    if not line or line.startswith('#'):
                        continue
                    
                    try:
                        parts = line.split()
                        if len(parts) >= 4:
                            # Format: node_id time freq psd
                            node_id = int(parts[0])
                            timestamp = float(parts[1])
                            freq = float(parts[2])
                            psd = float(parts[3])
                            data.append({
                                'node_id': node_id,
                                'time': timestamp,
                                'freq': freq,
                                'psd': psd
                            })
                        elif len(parts) >= 3:
                            # Format: time freq psd (no node_id)
                            timestamp = float(parts[0])
                            freq = float(parts[1])
                            psd = float(parts[2])
                            data.append({
                                'node_id': 0,
                                'time': timestamp,
                                'freq': freq,
                                'psd': psd
                            })
                    except (ValueError, IndexError):
                        continue
                
                # Update read position
                self.last_read_position[filepath] = f.tell()
                
        except Exception as e:
            logging.error(f"Error reading trace file: {e}")
            
        return data
    
    def read_time_window(self, filepath: str, start_time: float, end_time: float) -> np.ndarray:
        """Read data within a specific time window and return as 2D array"""
        if not filepath or not os.path.exists(filepath):
            return None
        
        data = []
        try:
            with open(filepath, 'r') as f:
                for line in f:
                    line = line.strip()
                    if not line or line.startswith('#'):
                        continue
                    
                    try:
                        parts = line.split()
                        if len(parts) >= 3:
                            if len(parts) >= 4:
                                timestamp = float(parts[1])
                                freq = float(parts[2])
                                psd = float(parts[3])
                            else:
                                timestamp = float(parts[0])
                                freq = float(parts[1])
                                psd = float(parts[2])
                            
                            if start_time <= timestamp <= end_time:
                                data.append([timestamp, freq, psd])
                    except (ValueError, IndexError):
                        continue
                        
        except Exception as e:
            logging.error(f"Error reading time window: {e}")
            return None
        
        if not data:
            return None
            
        return np.array(data)



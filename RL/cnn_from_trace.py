class SpectrumCNNProcessor:
    """Main processor that coordinates trace reading and CNN inference"""
    
    def __init__(self, config: Config):
        self.config = config
        self.reader = TraceFileReader(config.trace_dir)
        self.cnn = CNNInferenceEngine(config)
        self.running = False
        self.inference_count = 0
        
        # Track processed windows in-memory to prevent duplicates
        self._processed_windows = set()
        self._processing_lock = threading.Lock()
        
        # Create output directory
        os.makedirs(config.output_dir, exist_ok=True)
    
    def get_processed_windows(self) -> set:
        """Get set of already processed window indices"""
        processed = set()
        for f in os.listdir(self.config.output_dir):
            # Check for CNN_X.json (uppercase, as saved)
            if f.startswith('CNN_') and f.endswith('.json'):
                try:
                    # Extract index from CNN_1.json -> 1
                    idx = int(f.split('_')[1].split('.')[0])
                    processed.add(idx)
                except (ValueError, IndexError):
                    pass
        return processed
    
    def find_new_cnn_files(self) -> list:
        """Find new cnn_*.tr files that haven't been processed"""
        if not os.path.exists(self.config.trace_dir):
            return []
        
        # Combine disk-based and in-memory tracking
        processed_on_disk = self.get_processed_windows()
        
        with self._processing_lock:
            all_processed = processed_on_disk | self._processed_windows
        
        new_files = []
        
        for f in os.listdir(self.config.trace_dir):
            if f.startswith(self.config.cnn_trace_pattern) and f.endswith('.tr'):
                try:
                    # Extract index from cnn_1.tr -> 1
                    idx = int(f.split('_')[1].split('.')[0])
                    if idx not in all_processed:
                        filepath = os.path.join(self.config.trace_dir, f)
                        # Check if file has content (complete)
                        if os.path.getsize(filepath) > 0:
                            new_files.append((idx, filepath))
                except (ValueError, IndexError):
                    pass
        
        # Sort by index
        new_files.sort(key=lambda x: x[0])
        return new_files
    
    def wait_for_cnn_trace_file(self) -> str:
        """Wait for first cnn_1.tr file to be created"""
        first_file = os.path.join(self.config.trace_dir, 'cnn_1.tr')
        
        print(f"[CNN] Waiting for first trace file: {first_file}")
        logging.info(f"Waiting for first trace file: {first_file}")
        
        start_wait = time.time()
        
        while not os.path.exists(first_file):
            elapsed = time.time() - start_wait
            if elapsed > self.config.max_wait_time:
                print(f"[CNN] ERROR: First trace file not found after {self.config.max_wait_time}s")
                logging.error(f"First trace file not found after {self.config.max_wait_time}s")
                return None
            
            # Print status every 5 seconds
            if int(elapsed) % 5 == 0 and elapsed > 0:
                print(f"[CNN] Still waiting... ({elapsed:.0f}s elapsed)")
            
            time.sleep(0.1)  # Check every 100ms
        
        # File exists! Check if it has content
        while os.path.getsize(first_file) == 0:
            elapsed = time.time() - start_wait
            if elapsed > self.config.max_wait_time:
                print(f"[CNN] ERROR: Trace file exists but empty after {self.config.max_wait_time}s")
                return None
            time.sleep(0.01)  # Check every 10ms for content
        
        # Add 0.1ms buffer after file creation
        time.sleep(self.config.file_wait_buffer_ms / 1000.0)
        
        elapsed = time.time() - start_wait
        file_size = os.path.getsize(first_file)
        print(f"[CNN] First trace file ready! Size: {file_size} bytes (waited {elapsed:.2f}s)")
        logging.info(f"First trace file ready: {first_file}, size={file_size}, wait_time={elapsed:.2f}s")
        
        return first_file
        
    def process_band(self, band_name: str, current_time: float) -> dict:
        """Process a single band's spectrum data"""
        band_config = self.config.bands[band_name]
        
        # Find latest trace file for this band
        trace_file = self.reader.find_latest_trace(band_config['pattern'])
        if not trace_file:
            logging.warning(f"No trace file found for {band_name}")
            return None
        
        # Read last 100 seconds of data (or whatever is available)
        window_size = min(100.0, current_time)  # Don't exceed simulation time
        start_time = max(0, current_time - window_size)
        
        data = self.reader.read_time_window(trace_file, start_time, current_time)
        if data is None or len(data) == 0:
            logging.warning(f"No data in window [{start_time:.1f}, {current_time:.1f}] for {band_name}")
            return None
        
        print(f"[{band_name}] Read {len(data)} samples from {trace_file}")
        logging.info(f"[{band_name}] Processing {len(data)} samples, time=[{start_time:.1f}, {current_time:.1f}]")
        
        # Run CNN inference
        result = self.cnn.run_inference(data, band_name)
        if result:
            result['time_window'] = [start_time, current_time]
            result['sample_count'] = len(data)
            result['trace_file'] = os.path.basename(trace_file)
        
        return result
    
    def run_inference_cycle(self):
        """Run one inference cycle for all bands"""
        self.inference_count += 1
        timestamp = time.strftime('%Y%m%d_%H%M%S')
        current_sim_time = self.inference_count * self.config.inference_interval
        
        print(f"\n{'='*60}")
        print(f"CNN INFERENCE CYCLE #{self.inference_count}")
        print(f"Simulation time: {current_sim_time:.1f}s")
        print(f"{'='*60}")
        
        results = {}
        for band_name in self.config.bands:
            result = self.process_band(band_name, current_sim_time)
            if result:
                results[band_name] = result
                
                # Print detections
                print(f"\n[{band_name}] Detections:")
                for det in result['detections']:
                    bar = '█' * int(det['confidence'] * 20)
                    print(f"  {det['technology']:12s} {det['confidence']:.3f} {bar}")
        
        # Save results
        if results:
            output_file = os.path.join(
                self.config.output_dir,
                f'cnn_results_{timestamp}.json'
            )
            with open(output_file, 'w') as f:
                json.dump({
                    'timestamp': timestamp,
                    'results': results
                }, f, indent=2)
            print(f"\n[OUTPUT] Saved to {output_file}")
            logging.info(f"Saved results to {output_file}")
        
        return results
    
    def process_cnn_trace(self, trace_path: str) -> dict:
        """Process the cnn.tr file directly for CNN inference"""
        if not trace_path or not os.path.exists(trace_path):
            return None
        
        timestamp = time.strftime('%Y%m%d_%H%M%S')
        print(f"\n{'='*60}")
        print(f"[CNN] Processing cnn.tr (first 100ms spectrum data)")
        print(f"{'='*60}")
        
        # Read all data from the cnn.tr file (0 to 1s to capture all first 100ms data)
        data = self.reader.read_time_window(trace_path, 0.0, 1.0)  # Read all available data
        
        if data is None or len(data) == 0:
            print(f"[CNN] ERROR: No data found in {trace_path}")
            logging.error(f"No data found in {trace_path}")
            return None
        
        print(f"[CNN] Read {len(data)} samples from cnn.tr")
        logging.info(f"Read {len(data)} samples from cnn.tr")
        
        # Determine band from frequency range
        freq_col = data[:, 1]
        min_freq = freq_col.min()
        max_freq = freq_col.max()
        
        # Detect which band based on frequency range
        if min_freq < 3e9:
            band_name = '2.4GHz'
        else:
            band_name = '5GHz'
        
        print(f"[CNN] Detected band: {band_name} (freq range: {min_freq/1e9:.2f}-{max_freq/1e9:.2f} GHz)")
        
        # Run CNN inference
        result = self.cnn.run_inference(data, band_name)
        
        if result:
            result['time_window'] = [0.0, 0.1]
            result['sample_count'] = len(data)
            result['trace_file'] = os.path.basename(trace_path)
            result['freq_range'] = [float(min_freq), float(max_freq)]
            
            # Print detections
            print(f"\n[{band_name}] Detections:")
            for det in result['detections']:
                bar = '█' * int(det['confidence'] * 20)
                print(f"  {det['technology']:12s} {det['confidence']:.3f} {bar}")
            
            # Save results
            output_file = os.path.join(
                self.config.output_dir,
                f'cnn_result_{timestamp}.json'
            )
            with open(output_file, 'w') as f:
                json.dump({
                    'timestamp': timestamp,
                    'results': result
                }, f, indent=2)
            print(f"\n[OUTPUT] Saved to {output_file}")
            logging.info(f"Saved results to {output_file}")
        
        return result
    
    def process_single_window(self, window_idx: int, trace_path: str) -> dict:
        """Process a single cnn_X.tr file and save as CNN_X.json"""
        if not trace_path or not os.path.exists(trace_path):
            return None
        
        print(f"\n[CNN-{window_idx}] Processing {os.path.basename(trace_path)}...")
        
        # Add 0.1ms buffer
        time.sleep(self.config.file_wait_buffer_ms / 1000.0)
        
        # Read all data from the trace file
        data = self.reader.read_time_window(trace_path, 0.0, 1.0)
        
        if data is None or len(data) == 0:
            print(f"[CNN-{window_idx}] ERROR: No data found in {trace_path}")
            logging.error(f"Window {window_idx}: No data found in {trace_path}")
            return None
        
        # Determine band from frequency range
        freq_col = data[:, 1]
        min_freq = freq_col.min()
        max_freq = freq_col.max()
        band_name = '2.4GHz' if min_freq < 3e9 else '5GHz'
        
        print(f"[CNN-{window_idx}] Read {len(data)} samples, band: {band_name}")
        
        # Run CNN inference
        result = self.cnn.run_inference(data, band_name)
        
        if result:
            result['window_index'] = window_idx
            result['sample_count'] = len(data)
            result['trace_file'] = os.path.basename(trace_path)
            result['freq_range'] = [float(min_freq), float(max_freq)]
            result['processed_at'] = time.strftime('%Y-%m-%d %H:%M:%S.%f')[:-3]
            
            # Print detections
            print(f"[CNN-{window_idx}] Detections:")
            for det in result['detections']:
                bar = '█' * int(det['confidence'] * 20)
                print(f"  {det['technology']:12s} {det['confidence']:.3f} {bar}")
            
            # Save as CNN_X.json
            output_file = os.path.join(self.config.output_dir, f'CNN_{window_idx}.json')
            with open(output_file, 'w') as f:
                json.dump({
                    'window_index': window_idx,
                    'timestamp': time.strftime('%Y%m%d_%H%M%S'),
                    'results': result
                }, f, indent=2)
            
            print(f"[CNN-{window_idx}] Saved to {output_file}")
            logging.info(f"Window {window_idx} processed -> {output_file}")
        
        return result
    
    def monitor_and_process(self):
        """Continuously monitor for new cnn_X.tr files and process them in parallel"""
        from concurrent.futures import ThreadPoolExecutor
        
        self.running = True
        executor = ThreadPoolExecutor(max_workers=self.config.max_workers)
        pending_futures = {}
        
        print(f"\n{'='*60}")
        print(f"[CNN] PARALLEL MONITORING MODE")
        print(f"{'='*60}")
        print(f"Trace directory: {self.config.trace_dir}")
        print(f"Output directory: {self.config.output_dir}")
        print(f"Buffer after file: {self.config.file_wait_buffer_ms}ms")
        print(f"Max parallel workers: {self.config.max_workers}")
        print(f"Press Ctrl+C to stop\n")
        
        # Wait for first file
        print("[CNN] Waiting for first trace file (cnn_1.tr)...")
        first_file = self.wait_for_cnn_trace_file()
        if not first_file:
            print("[CNN] ERROR: First trace file never appeared. Exiting.")
            return
        
        try:
            while self.running:
                # Find new unprocessed files
                new_files = self.find_new_cnn_files()
                
                for window_idx, trace_path in new_files:
                    # Mark as processed immediately to prevent duplicates
                    with self._processing_lock:
                        if window_idx in self._processed_windows:
                            continue  # Already being processed
                        self._processed_windows.add(window_idx)
                    
                    print(f"[CNN] New file detected: cnn_{window_idx}.tr")
                    # Submit for parallel processing
                    future = executor.submit(self.process_single_window, window_idx, trace_path)
                    pending_futures[window_idx] = future
                
                # Clean up completed futures
                completed = [idx for idx, fut in pending_futures.items() if fut.done()]
                for idx in completed:
                    try:
                        pending_futures[idx].result()  # Check for exceptions
                    except Exception as e:
                        print(f"[CNN-{idx}] ERROR: {e}")
                        logging.error(f"Window {idx} processing failed: {e}")
                    del pending_futures[idx]
                
                # Small sleep to avoid busy-waiting
                time.sleep(0.05)  # Check every 50ms
                
        except KeyboardInterrupt:
            print("\n[CNN] Stopping...")
            self.running = False
        finally:
            # Wait for pending tasks
            if pending_futures:
                print(f"[CNN] Waiting for {len(pending_futures)} pending tasks...")
                for idx, fut in pending_futures.items():
                    try:
                        fut.result(timeout=5)
                    except:
                        pass
            executor.shutdown(wait=False)
            print("[CNN] Shutdown complete.")
    
    def run_once(self) -> dict:
        """Wait for cnn_1.tr, process it, and return results"""
        # Wait for the first trace file to be created
        trace_path = self.wait_for_cnn_trace_file()
        if not trace_path:
            return None
        
        # Process the first window
        return self.process_single_window(1, trace_path)
    
    def start(self):
        """Start the periodic inference loop"""
        self.running = True
        print(f"\n[CNN] Starting periodic inference (every {self.config.inference_interval}s)")
        print(f"[CNN] Monitoring: {self.config.trace_dir}")
        print(f"[CNN] Output: {self.config.output_dir}")
        print("[CNN] Press Ctrl+C to stop\n")
        
        try:
            while self.running:
                time.sleep(self.config.inference_interval)
                self.run_inference_cycle()
        except KeyboardInterrupt:
            print("\n[CNN] Stopping...")
            self.running = False
    
    def stop(self):
        self.running = False



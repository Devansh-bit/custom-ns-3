NS_LOG_COMPONENT_DEFINE ("SpectrumAnalyserLogger");

NS_OBJECT_ENSURE_REGISTERED (SpectrumAnalyserLogger);

// Initialize static members (multi-band shared files)
std::map<std::string, SpectrumAnalyserLogger::SharedFileInfo> SpectrumAnalyserLogger::s_sharedFiles;

void
SpectrumAnalyserLogger::EnableSharedFile(const std::string& bandId, const std::string& filePath)
{
  NS_LOG_FUNCTION(bandId << filePath);

  // Close existing file for this band if open
  if (s_sharedFiles.find(bandId) != s_sharedFiles.end())
    {
      if (s_sharedFiles[bandId].file && s_sharedFiles[bandId].file->is_open())
        {
          s_sharedFiles[bandId].file->close();
          delete s_sharedFiles[bandId].file;
        }
    }

  // Create new shared file info
  SharedFileInfo info;
  info.filePath = filePath;
  info.file = new std::ofstream(filePath.c_str());
  info.initialized = false;

  if (info.file->is_open())
    {
      info.initialized = true;
      // Write header
      *info.file << "# Spectrum PSD Trace File (.tr format)\n";
      *info.file << "# Band: " << bandId << "\n";
      *info.file << "# Format: node_id time_sec freq_hz psd_w_hz\n";
      *info.file << "# Generated by ns-3 SpectrumAnalyserLogger\n";
      NS_LOG_INFO("Shared .tr file opened for band '" << bandId << "': " << filePath);
    }
  else
    {
      NS_LOG_ERROR("Failed to open shared .tr file for band '" << bandId << "': " << filePath);
      delete info.file;
      info.file = nullptr;
    }

  s_sharedFiles[bandId] = info;
}

void
SpectrumAnalyserLogger::EnableSharedFile(const std::string& filePath)
{
  // Legacy single-argument version uses "default" band
  EnableSharedFile("default", filePath);
}

void
SpectrumAnalyserLogger::DisableSharedFile(const std::string& bandId)
{
  NS_LOG_FUNCTION(bandId);
  CloseSharedFile(bandId);
  s_sharedFiles.erase(bandId);
}

void
SpectrumAnalyserLogger::DisableSharedFile()
{
  NS_LOG_FUNCTION_NOARGS();
  CloseSharedFile();
  s_sharedFiles.clear();
}

bool
SpectrumAnalyserLogger::IsSharedFileEnabled()
{
  return !s_sharedFiles.empty();
}

bool
SpectrumAnalyserLogger::IsSharedFileEnabled(const std::string& bandId)
{
  auto it = s_sharedFiles.find(bandId);
  return (it != s_sharedFiles.end() && it->second.initialized);
}

std::string
SpectrumAnalyserLogger::GetSharedFilePath(const std::string& bandId)
{
  auto it = s_sharedFiles.find(bandId);
  if (it != s_sharedFiles.end())
    {
      return it->second.filePath;
    }
  return "";
}

std::string
SpectrumAnalyserLogger::GetSharedFilePath()
{
  // Legacy: return default band path
  return GetSharedFilePath("default");
}

void
SpectrumAnalyserLogger::CloseSharedFile(const std::string& bandId)
{
  NS_LOG_FUNCTION(bandId);
  auto it = s_sharedFiles.find(bandId);
  if (it != s_sharedFiles.end() && it->second.file)
    {
      if (it->second.file->is_open())
        {
          it->second.file->flush();
          it->second.file->close();
        }
      delete it->second.file;
      it->second.file = nullptr;
      it->second.initialized = false;
      NS_LOG_INFO("Shared .tr file closed for band '" << bandId << "'");
    }
}

void
SpectrumAnalyserLogger::CloseSharedFile()
{
  NS_LOG_FUNCTION_NOARGS();
  for (auto& pair : s_sharedFiles)
    {
      if (pair.second.file)
        {
          if (pair.second.file->is_open())
            {
              pair.second.file->flush();
              pair.second.file->close();
            }
          delete pair.second.file;
          pair.second.file = nullptr;
          pair.second.initialized = false;
        }
    }
  NS_LOG_INFO("All shared .tr files closed");
}

void
SpectrumAnalyserLogger::WriteEndMarker(const std::string& bandId, double timestamp)
{
  NS_LOG_FUNCTION(bandId << timestamp);
  auto it = s_sharedFiles.find(bandId);
  if (it != s_sharedFiles.end() && it->second.file && it->second.file->is_open())
    {
      // Write END marker with exact stop timestamp
      // Format: "# END <timestamp>" - comment line that parsers can use to know exact stop time
      *it->second.file << "# END " << timestamp << "\n";
      it->second.file->flush();
      NS_LOG_INFO("Wrote END marker at t=" << timestamp << "s to band '" << bandId << "'");
    }
}

void
SpectrumAnalyserLogger::WriteEndMarker(double timestamp)
{
  NS_LOG_FUNCTION(timestamp);
  for (auto& pair : s_sharedFiles)
    {
      if (pair.second.file && pair.second.file->is_open())
        {
          *pair.second.file << "# END " << timestamp << "\n";
          pair.second.file->flush();
          NS_LOG_INFO("Wrote END marker at t=" << timestamp << "s to band '" << pair.first << "'");
        }
    }
}

void
SpectrumAnalyserLogger::CloseSharedFileWithTimestamp(const std::string& bandId, double finalTimestamp)
{
  NS_LOG_FUNCTION(bandId << finalTimestamp);
  WriteEndMarker(bandId, finalTimestamp);
  CloseSharedFile(bandId);
}

void
SpectrumAnalyserLogger::CloseAllSharedFilesWithTimestamp(double finalTimestamp)
{
  NS_LOG_FUNCTION(finalTimestamp);
  WriteEndMarker(finalTimestamp);
  CloseSharedFile();
}


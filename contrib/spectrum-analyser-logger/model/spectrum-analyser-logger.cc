SpectrumAnalyserLogger::SpectrumAnalyserLogger ()
  : m_loggingInterval (MilliSeconds (100)),  // Default: 100ms
    m_lastLogTime (Seconds (0.0)),
    m_outputFilename (""),
    m_nodeId (0),
    m_consoleOutput (false),
    m_fileOutputEnabled (false)
{
  NS_LOG_FUNCTION (this);
}

SpectrumAnalyserLogger::~SpectrumAnalyserLogger ()
{
  NS_LOG_FUNCTION (this);
  if (m_outputFile.is_open ())
    {
      m_outputFile.close ();
    }
}

void
SpectrumAnalyserLogger::SetLoggingInterval (Time interval)
{
  NS_LOG_FUNCTION (this << interval);
  m_loggingInterval = interval;
}

Time
SpectrumAnalyserLogger::GetLoggingInterval (void) const
{
  return m_loggingInterval;
}

void
SpectrumAnalyserLogger::SetOutputFile (const std::string& filename)
{
  NS_LOG_FUNCTION (this << filename);
  m_outputFilename = filename;

  // Close existing file if open
  if (m_outputFile.is_open ())
    {
      m_outputFile.close ();
    }

  // Open new file
  m_outputFile.open (filename.c_str ());
  if (m_outputFile.is_open ())
    {
      m_fileOutputEnabled = true;
      // Write header
      m_outputFile << "# Spectrum Analyzer Logger Data\n";
      m_outputFile << "# NodeId: " << m_nodeId << "\n";
      m_outputFile << "# Format: Timestamp(s) Frequency(Hz) PSD(W/Hz)\n";
      NS_LOG_INFO ("Opened output file: " << filename);
    }
  else
    {
      NS_LOG_ERROR ("Failed to open output file: " << filename);
      m_fileOutputEnabled = false;
    }
}

void
SpectrumAnalyserLogger::SetNodeId (uint32_t nodeId)
{
  NS_LOG_FUNCTION (this << nodeId);
  m_nodeId = nodeId;
}

void
SpectrumAnalyserLogger::SetConsoleOutput (bool enable)
{
  NS_LOG_FUNCTION (this << enable);
  m_consoleOutput = enable;
}

void
SpectrumAnalyserLogger::PsdCallback (Ptr<const SpectrumValue> psd)
{
  NS_LOG_FUNCTION (this << psd);

  double timestamp = Simulator::Now ().GetSeconds ();

  // Check if enough time has passed since last log
  if ((timestamp - m_lastLogTime.GetSeconds ()) < m_loggingInterval.GetSeconds ())
    {
      return;
    }

  m_lastLogTime = Seconds (timestamp);

  // Write PSD data
  WritePsdData (timestamp, psd);
}

void
SpectrumAnalyserLogger::WritePsdData (double timestamp, Ptr<const SpectrumValue> psd)
{
  NS_LOG_FUNCTION (this << timestamp << psd);

  // Wait for basic-simulation to reach this timestamp (sync barrier)
  const double tolerance = 0.05;  // 50ms tolerance
  int maxWait = 1000;  // Max 10s wait (1000 * 10ms)
  for (int i = 0; i < maxWait; i++)
    {
      std::ifstream syncFile("/tmp/ns3-sync-timestamp.txt");
      double leaderTime = 0.0;
      if (syncFile >> leaderTime && leaderTime >= timestamp - tolerance)
        {
          break;  // Leader caught up, proceed with write
        }
      syncFile.close();
      usleep(10000);  // Wait 10ms and check again
    }

  if (!psd)
    {
      NS_LOG_WARN ("Received null PSD pointer");
      return;
    }

  Values::const_iterator it = psd->ConstValuesBegin ();
  Bands::const_iterator bit = psd->ConstBandsBegin ();

  while (it != psd->ConstValuesEnd ())
    {
      double frequency = bit->fc;  // Center frequency
      double psdValue = (*it);      // PSD value in W/Hz

      // Write to shared .tr file if enabled (format: node_id time freq psd)
      auto sharedIt = s_sharedFiles.find(m_bandId.empty() ? "default" : m_bandId);
      if (sharedIt != s_sharedFiles.end() && sharedIt->second.initialized && sharedIt->second.file && sharedIt->second.file->is_open())
        {
          *sharedIt->second.file << m_nodeId << " "
                                 << timestamp << " "
                                 << frequency << " "
                                 << psdValue << "\n";
        }

      // Write to per-node file if enabled (original behavior)
      if (m_fileOutputEnabled && m_outputFile.is_open ())
        {
          m_outputFile << timestamp << " "
                      << frequency << " "
                      << psdValue << std::endl;
        }

      // Write to console if enabled
      if (m_consoleOutput)
        {
          std::cout << "Node" << m_nodeId << " "
                    << timestamp << " "
                    << frequency << " "
                    << psdValue << std::endl;
        }

      ++it;
      ++bit;
    }

  // Flush shared file periodically
  auto flushIt = s_sharedFiles.find(m_bandId.empty() ? "default" : m_bandId);
  if (flushIt != s_sharedFiles.end() && flushIt->second.initialized && flushIt->second.file && flushIt->second.file->is_open())
    {
      flushIt->second.file->flush();
    }

  // Flush per-node file buffer periodically for real-time monitoring
  if (m_fileOutputEnabled && m_outputFile.is_open ())
    {
      m_outputFile.flush ();
    }

  // Write spectrum-shadow sync timestamp for bidirectional synchronization
  // This allows basic-simulation to wait for spectrum-shadow to catch up
  {
    std::ofstream spectrumSyncFile("/tmp/ns3-spectrum-sync-timestamp.txt", std::ios::trunc);
    if (spectrumSyncFile.is_open())
      {
        spectrumSyncFile << std::fixed << std::setprecision(6) << timestamp;
        spectrumSyncFile.close();
      }
  }
}

